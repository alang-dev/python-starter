		self.symbol = data.get('symbol')
		self.price_high = data.get('priceHigh')
		self.price_low = data.get('priceLow')
		self.price_open = data.get('priceOpen')
		self.price_average = data.get('priceAverage')
		self.price_close = data.get('priceClose')
		self.price_basic = data.get('priceBasic')
		self.total_volume = data.get('totalVolume')
		self.deal_volume = data.get('dealVolume')
		self.put_through_volume = data.get('putthroughVolume')
		self.total_value = data.get('totalValue')
		self.put_through_value = data.get('putthroughValue')
		self.buy_foreign_quantity = data.get('buyForeignQuantity')
		self.buy_foreign_value = data.get('buyForeignValue')
		self.sell_foreign_quantity = data.get('sellForeignQuantity')
		self.sell_foreign_value = data.get('sellForeignValue')
		self.buy_count = data.get('buyCount')
		self.buy_quantity = data.get('buyQuantity')
		self.sell_count = data.get('sellCount')
		self.sell_quantity = data.get('sellQuantity')
		self.adj_ratio = data.get('adjRatio')
		self.current_foreign_room = data.get('currentForeignRoom')
		self.prop_trading_net_deal_value = data.get('propTradingNetDealValue')
		self.prop_trading_net_pt_value = data.get('propTradingNetPTValue')
		self.prop_trading_net_value = data.get('propTradingNetValue')
		self.unit = data.get('unit')
